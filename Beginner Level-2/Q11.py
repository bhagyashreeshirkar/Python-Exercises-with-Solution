# 1. Write a Python program to create a tuple.
# Method-1
tuple0 = ()
print(tuple0)

# Method-2
tuple1 = tuple()
print(tuple1)


# 2. Write a Python program to create a tuple with different data types.
tuple2 = (2, 3.7, 'Kevin', True)
print(tuple2)


# 3. Write a python program to create and print a tuple with single item.
tuple3 = (1,)  # if you don't put a comma after the number, then it'll print single number not a tuple
print(tuple3)
